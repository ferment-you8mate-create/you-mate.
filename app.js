function unique(values) {
  return [...new Set(values)].sort((a, b) => a.localeCompare(b, "ja"));
}

function normalize(text) {
  return text.toLowerCase().replace(/\s+/g, "");
}

function contentHaystack(item) {
  return normalize([
    item.title,
    item.region,
    item.category,
    item.useCase,
    item.summary,
    item.story || "",
    (item.details || []).join(" "),
    item.tags.join(" "),
    item.points.join(" "),
    item.ideas.join(" ")
  ].join(" "));
}

function option(value) {
  const el = document.createElement("option");
  el.value = value;
  el.textContent = value;
  return el;
}

function setupFilters() {
  unique(contents.map((item) => item.category)).forEach((category) => {
    categoryFilter.appendChild(option(category));
  });

  unique(contents.map((item) => item.region)).forEach((region) => {
    regionFilter.appendChild(option(region));
  });

  const tags = unique(contents.flatMap((item) => item.tags));
  tags.forEach((tag) => {
    const button = document.createElement("button");
    button.className = "tag-button";
    button.dataset.tag = tag;
    button.type = "button";
    button.textContent = tag;
    button.addEventListener("click", () => {
      state.tag = tag;
      updateTagButtons();
      render();
    });
    tagButtonsEl.appendChild(button);
  });

  document.querySelector('[data-tag="all"]').addEventListener("click", () => {
    state.tag = "all";
    updateTagButtons();
    render();
  });

  document.querySelector("#contentCount").textContent = contents.length;
  document.querySelector("#tagCount").textContent = tags.length;
  document.querySelector("#regionCount").textContent = unique(contents.map((item) => item.region)).length;

  const counts = contents.reduce((acc, item) => {
    acc[item.category] = (acc[item.category] || 0) + 1;
    return acc;
  }, {});

  categoryStatsEl.innerHTML = Object.entries(counts)
    .sort((a, b) => a[0].localeCompare(b[0], "ja"))
    .map(([category, count]) => `<div class="stat-item"><span>${category}</span><strong>${count}</strong></div>`)
    .join("");
}

function updateTagButtons() {
  document.querySelectorAll(".tag-button").forEach((button) => {
    button.classList.toggle("active", button.dataset.tag === state.tag);
  });
}

function filteredContents() {
  const query = normalize(state.query);
  return contents.filter((item) => {
    const matchesQuery = query.length === 0 || contentHaystack(item).includes(query);
    const matchesCategory = state.category === "all" || item.category === state.category;
    const matchesRegion = state.region === "all" || item.region === state.region;
    const matchesTag = state.tag === "all" || item.tags.includes(state.tag);
    return matchesQuery && matchesCategory && matchesRegion && matchesTag;
  });
}

function renderCard(item) {
  const card = document.createElement("article");
  card.className = "card";
  card.innerHTML = `
    <div class="card-top">
      <span class="category">${item.category}</span>
      <span class="region">${item.region}</span>
    </div>
    <h3>${item.title}</h3>
    <p>${item.summary}</p>
    ${item.story ? `<p class="card-story">${item.story}</p>` : ""}
    <div class="tags">${item.tags.map((tag) => `<span class="tag">${tag}</span>`).join("")}</div>
    <div class="card-footer">
      <span class="use-case">${item.useCase}</span>
      <button class="card-button" type="button">詳細を見る</button>
    </div>
  `;
  card.querySelector(".card-button").addEventListener("click", () => openDetail(item));
  return card;
}

function render() {
  const items = filteredContents();
  cardsEl.innerHTML = "";
  items.forEach((item) => cardsEl.appendChild(renderCard(item)));
  resultCountEl.textContent = `${items.length}件`;
  emptyStateEl.hidden = items.length > 0;
}

